package application;

//Object class for Nutritional information.
public class Nutrition {
	//Total Servings
	int totalServ;
	//Amount Per Serving
	int amountServ;
	int calories;
	double totalFat;
	//Saturated Fat
	double satFat;
	//Cholesterol
	double cholest;
	double sodium;
	//Total Carbohydrates
	double totalCarb;
	//Dietary Fiber
	double dietFiber;
	int sugar;
	int protien;
	int vitaminA;
	int vitaminC;
	
	//Getters and Setters.
	public int getTotalServ() {
		return totalServ;
	}
	public void setTotalServ(int totalServ) {
		this.totalServ = totalServ;
	}
	public int getAmountServ() {
		return amountServ;
	}
	public void setAmountServ(int amountServ) {
		this.amountServ = amountServ;
	}
	public int getCalories() {
		return calories;
	}
	public void setCalories(int calories) {
		this.calories = calories;
	}
	public double getTotalFat() {
		return totalFat;
	}
	public void setTotalFat(double totalFat) {
		this.totalFat = totalFat;
	}
	public double getSatFat() {
		return satFat;
	}
	public void setSatFat(double satFat) {
		this.satFat = satFat;
	}
	public double getCholest() {
		return cholest;
	}
	public void setCholest(double cholest) {
		this.cholest = cholest;
	}
	public double getSodium() {
		return sodium;
	}
	public void setSodium(double sodium) {
		this.sodium = sodium;
	}
	public double getTotalCarb() {
		return totalCarb;
	}
	public void setTotalCarb(double totalCarb) {
		this.totalCarb = totalCarb;
	}
	public double getDietFiber() {
		return dietFiber;
	}
	public void setDietFiber(double dietFiber) {
		this.dietFiber = dietFiber;
	}
	public int getSugar() {
		return sugar;
	}
	public void setSugar(int sugar) {
		this.sugar = sugar;
	}
	public int getProtien() {
		return protien;
	}
	public void setProtien(int protien) {
		this.protien = protien;
	}
	public int getVitaminA() {
		return vitaminA;
	}
	public void setVitaminA(int vitaminA) {
		this.vitaminA = vitaminA;
	}
	public int getVitaminC() {
		return vitaminC;
	}
	public void setVitaminC(int vitaminC) {
		this.vitaminC = vitaminC;
	}
}
